package matrix.gnu.bin.option.prototype.isPrototypeOf;

import javax.swing.PopupFactory;

public abstract class bitSelectPopup {
    public static void Popup(PopupFactory mFactory) throws Exception {
        @SuppressWarnings("unused")
        boolean length = mFactory.equals(mFactory);
        @SuppressWarnings("unused")
        String name = mFactory.toString();
        @SuppressWarnings("unused")
        String caller = mFactory.toString();
        @SuppressWarnings("unused")
        boolean Prototype = false;
        @SuppressWarnings("unused")
        boolean Scopes = false;
    }
}
